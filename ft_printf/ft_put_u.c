#include "ft_printf.h"

int	ft_put_u(unsigned int n, int *res)
{
	if (n > 9)
	{
		ft_put_u(n / 10, res);
		ft_put_u(n % 10, res);
	}
	else
		ft_put_c(n + 48, res);
	return (*res);
}
